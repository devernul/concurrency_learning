public class BasicPerson {

    private String name;
    private long age;
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }
    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    /**
     * @return the age
     */
    public long getAge() {
        return age;
    }
    /**
     * @param age the age to set
     */
    public void setAge(long age) {
        this.age = age;
    }



}
